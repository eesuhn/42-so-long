#include "../libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	if (!s || !fd)
		return ;
	i = 0;
	while (*(s + i++))
		ft_putchar_fd(*(s + i - 1), fd);
}
